/** 根据宜/忌文案匹配小图标 */
export function getListIcon(text, type) {
  const t = text || ''
  if (type === 'suitable') {
    if (/热汤|清汤|汤|粥/.test(t)) return listIcons.soup
    if (/慢慢吃|慢|不挑|认真/.test(t)) return listIcons.slow
    if (/饭搭子|一起|分享|聚餐|朋友/.test(t)) return listIcons.friend
    if (/散步|早一点/.test(t)) return listIcons.walk
    if (/蔬|青菜|水果|清爽|轻/.test(t)) return listIcons.leaf
    if (/配.*小菜|配菜|配米饭|配蔬果|配凉茶|配一碟/.test(t)) return listIcons.plate
    if (/喝水|水|茶/.test(t)) return listIcons.drop
    if (/奶香|奶/.test(t)) return listIcons.milk
    if (/微辣|辣/.test(t)) return listIcons.chili
    if (/简单|朴素|一次吃完/.test(t)) return listIcons.check
    if (/心无旁骛|仪式/.test(t)) return listIcons.heart
    return listIcons.heart
  } else {
    if (/奶茶/.test(t)) return listIcons.milktea
    if (/冰美式|咖啡/.test(t)) return listIcons.coffee
    if (/外卖软件/.test(t)) return listIcons.phone
    if (/朋友|较真/.test(t)) return listIcons.chat
    if (/零食/.test(t)) return listIcons.cookie
    if (/边走边吃|走/.test(t)) return listIcons.walkx
    if (/连续|点同/.test(t)) return listIcons.repeat
    if (/炸鸡|报复/.test(t)) return listIcons.drumstick
    if (/配送费|配送/.test(t)) return listIcons.coin
    if (/只喝|不吃饭|空腹/.test(t)) return listIcons.no
    return listIcons.no
  }
}

